-module(float_to_short).

-export([start/0, id/1, negate/1]).

start() ->
    ok = test_short_basic(),
    ok = test_short_grisu3(),
    ok = test_short_edge_cases(),
    ok = test_short_combinations(),
    ok = test_short_badarg(),
    ok = test_short_list(),
    ok = test_1000_float_list(),
